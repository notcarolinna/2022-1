#include <math.h>  //para poder usar o pow
#include <stdio.h>

int main() {
  // Escreva um programa que recebe dois inteiros a e b e calcula a^b.

  int base, expoente, potencia;
  printf("Digite o valor da base e do expoente:\n");
  scanf("%d%d", &base, &expoente);

  potencia = pow(base, expoente);

  printf("O resultado da potência é: %d!", potencia);
}
